    public void Confirmacao() throws Exception{
        String utilizador,nome,passe;
        int id=0;
        utilizador= ctx_Utilizador.getText();
        Connection con = ConnectionFactory.getConnection().Conn();
        String query = "SELECT * FROM administradores WHERE administrador='"+utilizador+"'";
        PreparedStatement stmt = con.prepareStatement(query);
        ResultSet resultado = stmt.executeQuery();
        while(resultado.next()){
        id = Integer.parseInt(resultado.getString("id_admin"));
        }
        if(id==0){
            JOptionPane.showMessageDialog(this,"Utilizador n√£o existente!", "Erro", JOptionPane.ERROR_MESSAGE);
        }else{
            if(txt_password.getText().equals(txt_password1.getText())){
                passe = txt_password.getText();
                String query1 = "UPDATE administradores SET passe="+passe+" WHERE id_admin="+id;
                PreparedStatement stmt1 = con.prepareStatement(query1);
                stmt1.execute();
                JOptionPane.showMessageDialog(this,"Palavra-passe alterada com sucesso!", "Confirma√ß√£o", JOptionPane.INFORMATION_MESSAGE);
                this.dispose();
            }else{
                JOptionPane.showMessageDialog(this,"As Palavra-passes n√£o correspondem!", "Erro", JOptionPane.ERROR_MESSAGE);
            }
            
        }
        
    }
    
