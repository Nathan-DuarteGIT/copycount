    public void Entrar() throws Exception{
        String utilizador, passe, nome, passe1 = null;
        int id=0;
        utilizador= txt_utilizadorLogin.getText();
        passe= txt_password.getText();
        Connection con = ConnectionFactory.getConnection().Conn();
        String query = "SELECT * FROM administradores WHERE administrador='"+utilizador+"'";
        PreparedStatement stmt = con.prepareStatement(query);
        ResultSet resultado = stmt.executeQuery();
        while(resultado.next()){
            id = Integer.parseInt(resultado.getString("id_admin"));
            nome = resultado.getString("administrador");
            passe1 = resultado.getString("passe");
        }
        if(id==0){
            JOptionPane.showMessageDialog(this,"Utilizador ou passe incorretos", "Erro", JOptionPane.ERROR_MESSAGE);
        }else{
            MenuPrincipal m = new MenuPrincipal();
            if(passe.equals(passe1)){
                this.dispose();
                m.setVisible(true);
            }else
                JOptionPane.showMessageDialog(this,"Utilizador ou passe incorretos", "Erro", JOptionPane.ERROR_MESSAGE);
        }
        
    }
    
